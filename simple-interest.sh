# Contributing to Simple Interest Calculator

We welcome contributions to the Simple Interest Calculator project! Please follow these guidelines when contributing:

## How to Contribute

1. **Fork the repository**: Click on the "Fork" button at the top right corner of this page.
2. **Clone your fork**: 
    ```bash
    git clone https://github.com/yourusername/simple-interest-calculator.git
    ```
3. **Create a branch**:
    ```bash
    git checkout -b feature-name
    ```
4. **Make your changes**: Make sure to follow the coding standards.
5. **Commit your changes**:
    ```bash
    git commit -m "Description of the feature or fix"
    ```
6. **Push to your fork**:
    ```bash
    git push origin feature-name
    ```
7. **Create a pull request**: Go to the original repository and click on "New pull request".

## Coding Standards

- Follow PEP 8 for Python code.
- Write clear, concise commit messages.

## Reporting Issues

If you find a bug or have a feature request, please create an issue in the [Issue Tracker](https://github.com/yourusername/simple-interest-calculator/issues).

Thank you for contributing!
